print("FOR : ")
for letra in 'aeiou':
    print (letra)

# Com while
print("while")
texto = 'aeiou'
k = 0
while k < len (texto):
    letra = texto[k]
    print (letra)
    k = k+1

# for  com range
print("Range Com FOR")
for i in range(5):
    print (i)


# Com while
print("Range Com while")
lista = list(range(5))
k = 0
while k < len (lista):
    i = lista[k]
    print (i)
    k = k+1
